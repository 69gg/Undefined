from __future__ import annotations

# 导入
import asyncio

# 导入
import json

# 导入
import logging

# 导入
from typing import Any

# 导入
import httpx

# 导入
from Undefined.skills.http_config import (
    get_request_proxy,
    get_request_retries,
    get_request_timeout,
)

# 赋值
logger = logging.getLogger(__name__)


# 函数 _should_retry_http_status
def _should_retry_http_status(status_code: int) -> bool:
    # 返回
    return status_code == 429 or 500 <= status_code < 600


# 函数 _retry_delay
def _retry_delay(attempt: int) -> float:
    # 返回
    return float(min(2.0, 0.25 * (2**attempt)))


# 异步函数 request_with_retry
async def request_with_retry(
    method: str,
    url: str,
    *,
    params: dict[str, Any] | None = None,
    json_data: Any | None = None,
    data: Any | None = None,
    files: Any | None = None,
    headers: dict[str, str] | None = None,
    timeout: float | None = None,
    default_timeout: float = 480.0,
    follow_redirects: bool = False,
    context: dict[str, Any] | None = None,
    retries: int | None = None,
) -> httpx.Response:
    # 赋值
    request_timeout = (
        timeout if timeout is not None else get_request_timeout(default_timeout)
    )
    # 赋值
    request_retries = retries if retries is not None else get_request_retries(0)
    # 赋值
    request_proxy = get_request_proxy(url)
    # 赋值
    request_id = "-"
    # 条件分支
    if context is not None:
        # 赋值
        request_id = str(context.get("request_id", "-"))

    # 注解赋值
    last_exception: Exception | None = None
    # 注解赋值
    client_kwargs: dict[str, Any] = {
        "timeout": request_timeout,
        "follow_redirects": follow_redirects,
        "trust_env": False,
    }
    # 条件分支
    if request_proxy is not None:
        # 赋值
        client_kwargs["proxy"] = request_proxy

    # async with 上下文
    async with httpx.AsyncClient(**client_kwargs) as client:
        # for 循环
        for attempt in range(request_retries + 1):
            # try 块
            try:
                # 赋值
                response = await client.request(
                    method=method,
                    url=url,
                    params=params,
                    json=json_data,
                    data=data,
                    files=files,
                    headers=headers,
                )
                # 条件分支
                if (
                    _should_retry_http_status(response.status_code)
                    and attempt < request_retries
                ):
                    # 赋值
                    delay = _retry_delay(attempt)
                    # 表达式
                    logger.warning(
                        "[HTTP] status retry: method=%s url=%s status=%s attempt=%s/%s wait=%.2fs request_id=%s",
                        method,
                        url,
                        response.status_code,
                        attempt + 1,
                        request_retries + 1,
                        delay,
                        request_id,
                    )
                    # 表达式
                    await asyncio.sleep(delay)
                    # continue
                    continue

                # 表达式
                response.raise_for_status()
                # 返回
                return response
            except httpx.HTTPStatusError as exc:
                # 赋值
                last_exception = exc
                # 条件分支
                if attempt >= request_retries:
                    # break
                    break
                # 条件分支
                if not _should_retry_http_status(exc.response.status_code):
                    # break
                    break
                # 赋值
                delay = _retry_delay(attempt)
                # 表达式
                logger.warning(
                    "[HTTP] status exception retry: method=%s url=%s status=%s attempt=%s/%s wait=%.2fs request_id=%s",
                    method,
                    url,
                    exc.response.status_code,
                    attempt + 1,
                    request_retries + 1,
                    delay,
                    request_id,
                )
                # 表达式
                await asyncio.sleep(delay)
            except (httpx.TimeoutException, httpx.RequestError) as exc:
                # 赋值
                last_exception = exc
                # 条件分支
                if attempt >= request_retries:
                    # break
                    break
                # 赋值
                delay = _retry_delay(attempt)
                # 表达式
                logger.warning(
                    "[HTTP] request retry: method=%s url=%s err=%s attempt=%s/%s wait=%.2fs request_id=%s",
                    method,
                    url,
                    type(exc).__name__,
                    attempt + 1,
                    request_retries + 1,
                    delay,
                    request_id,
                )
                # 表达式
                await asyncio.sleep(delay)

    # 条件分支
    if last_exception is not None:
        # 抛出异常
        raise last_exception
    # 抛出异常
    raise RuntimeError(f"HTTP request failed without exception: {method} {url}")


# 异步函数 get_json_with_retry
async def get_json_with_retry(
    url: str,
    *,
    params: dict[str, Any] | None = None,
    timeout: float | None = None,
    default_timeout: float = 480.0,
    follow_redirects: bool = False,
    context: dict[str, Any] | None = None,
    retries: int | None = None,
) -> Any:
    # 赋值
    request_id = "-"
    # 条件分支
    if context is not None:
        # 赋值
        request_id = str(context.get("request_id", "-"))

    # 赋值
    response = await request_with_retry(
        "GET",
        url,
        params=params,
        timeout=timeout,
        default_timeout=default_timeout,
        follow_redirects=follow_redirects,
        context=context,
        retries=retries,
    )
    # try 块
    try:
        # 返回
        return response.json()
    except json.JSONDecodeError as exc:
        # 赋值
        content_type = response.headers.get("content-type", "")
        # 赋值
        preview = response.text[:200].replace("\n", "\\n").replace("\r", "\\r")
        # 表达式
        logger.warning(
            "[HTTP] json decode failed: url=%s status=%s content_type=%s preview=%s request_id=%s err=%s",
            url,
            response.status_code,
            content_type,
            preview,
            request_id,
            exc,
        )
        # 抛出异常
        raise


